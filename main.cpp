static float zoomIn = 0.5;
static float zoomOut = 1.5;

void zoom(float a_ratio, float a_mouseX, float a_mouseY)
{
    Point topLeftWindowToMouseCenter;
    Point mouseViewport(a_mouseX, a_mouseY);

    Point mouseWorld;
    transformPointBetweenRects(mouseWorld, mouseViewport,
                               viewport, world);

    translate(topLeftWindowToMouseCenter,
              window.topLeft,
              -mouseWorld.x,
              -mouseWorld.y);

    Point topLeftScaled;

    float zoomFactor = a_ratio;
    
    scale(topLeftScaled,
          topLeftWindowToMouseCenter,
          zoomFactor,
          zoomFactor);

    Point topLeftTranslatedToOriginalPosition;
    translate(topLeftTranslatedToOriginalPosition,
              topLeftScaled,
              mouseWorld.x,
              mouseWorld.y);

    window.topLeft = topLeftTranslatedToOriginalPosition;	
}

static gboolean clicked(GtkWidget *widget, GdkEventButton *event,
    gpointer user_data)
{
    if (event->button == 1)
    {
    	zoom(zoomIn, event->x, event->y);
        gtk_widget_queue_draw(widget);
    }

    if (event->button == 3)
    {
    	zoom(zoomOut, event->x, event->y);
        gtk_widget_queue_draw(widget);
    }

    return TRUE;
}

static void do_drawing(cairo_t *cr)
{
  cairo_set_source_rgb(cr, 0, 0, 0);
  cairo_set_line_width(cr, 0.5);

  Quadrilateral shape01;

  shape01.points[0] = Point(-170.f, 80.f);
  shape01.points[1] = Point(-120.f, 80.f);
  shape01.points[2] = Point(-170.f, 30.f);
  shape01.points[3] = Point(-120.f, 30.f);

  Point windowPoint(180.f, -90.f);
  Point viewportPoint;

  transformPointBetweenRects(viewportPoint, windowPoint,
                             window, viewport);

  Point cearaViewport[559];

  for(int i = 0; i < 559; ++i)
  {
      Point newPoint;

      transformPointBetweenRects(newPoint, ceara[i],
                                 window, viewport);

      cearaViewport[i].x = newPoint.x;
      cearaViewport[i].y = newPoint.y;

      cairo_arc(cr, cearaViewport[i].x, cearaViewport[i].y, 2, 0, 2 * M_PI);
      cairo_stroke(cr);
  }



  cairo_stroke(cr);
}

static gboolean on_draw_event(GtkWidget *widget, cairo_t *cr,
    gpointer user_data)
{
  do_drawing(cr);

  return FALSE;
}



int main(int argc, char** argv)
{
    GtkWidget *gtkwindow;
    GtkWidget *darea;
    gtk_init(&argc, &argv);

    /* Create the main, top level window */
    gtkwindow = gtk_window_new(GTK_WINDOW_TOPLEVEL);

    darea = gtk_drawing_area_new();
    gtk_container_add(GTK_CONTAINER(gtkwindow), darea);

    /* Give it the title */
    gtk_window_set_title(GTK_WINDOW(gtkwindow), "Canvas");

    /* Center the window */
    gtk_window_set_position(GTK_WINDOW(gtkwindow), GTK_WIN_POS_CENTER);

    /* Set the window's default size */
    gtk_window_set_default_size(GTK_WINDOW(gtkwindow), 640, 480);

    g_signal_connect(G_OBJECT(darea), "draw", G_CALLBACK(on_draw_event), NULL);
    g_signal_connect(gtkwindow, "button-press-event", G_CALLBACK(clicked), NULL);
    g_signal_connect(gtkwindow, "destroy", G_CALLBACK(gtk_main_quit), NULL);

    gtk_widget_show_all(gtkwindow);

    /*
    ** Start the main loop, and do nothing (block) until
    ** the application is closed
    */
    gtk_main();

    return 0;
}
